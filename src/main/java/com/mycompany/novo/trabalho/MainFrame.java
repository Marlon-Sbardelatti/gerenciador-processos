	private int cadastrarAudienciaEmProcesso(int numTribunal, int numVara, String uf, int codigoProcesso, Date data, String descricao){
		boolean foundTribunal = false;
		boolean foundVara = false;
		boolean foundProcesso = false;
		int error = 0;

		for(Tribunal t : tribunais){
			if (t.getNumero() == numTribunal && uf.toUpperCase().equals(t.getUf())) {
				foundTribunal = true;
				for(Vara v : t.getVaras()){
					if (v.getNum() == numVara) {
						foundVara = true;
						for(Processo p : v.getProcessos()){
							if (p.getCodigo() == codigoProcesso) {
								foundProcesso = true;
								Audiencia a = new Audiencia(data, descricao);
								p.addAudiencia(a);
							}
						}
					}
				}
			}
		}


		if (!foundTribunal) {
			error = 1;
		} else {
			if (!foundVara) {
				error = 2;
			}else {
				if (!foundProcesso) {
					error = 3;
				}
			}
		}
		return error;
	}

