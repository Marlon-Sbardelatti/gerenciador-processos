	//verifica a existencia e retorna o possivel erro(int) not found, ja add o custo para aproveitar o for.
	private int cadastrarCustoEmProcesso(int numTribunal, int numVara, String uf, int codigoProcesso, Date data, String descricao, double val){
		boolean foundTribunal = false;
		boolean foundVara = false;
		boolean foundProcesso = false;
		int error = 0;

		for(Tribunal t : tribunais){
			if (t.getNumero() == numTribunal && uf.toUpperCase().equals(t.getUf())) {
				foundTribunal = true;
				for(Vara v : t.getVaras()){
					if (v.getNum() == numVara) {
						foundVara = true;
						for(Processo p : v.getProcessos()){
							if (p.getCodigo() == codigoProcesso) {
								foundProcesso = true;
								Custo c = new Custo(data, descricao, val);
								p.addCusto(c);
							}
						}
					}
				}
			}
		}


		if (!foundTribunal) {
			error = 1;
		} else {
			if (!foundVara) {
				error = 2;
			}else {
				if (!foundProcesso) {
					error = 3;
				}
			}
		}
		return error;
	}

