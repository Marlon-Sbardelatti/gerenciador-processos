	//retorna os custos do processo x
	private String retornarCustosProcesso(int codigoProcesso, int numVara, int numTribunal, String uf){
		DecimalFormat df = new DecimalFormat("0.00");
		String dados = "Custos:\n\n";
		for (Tribunal t : tribunais){
			if (t.getNumero() == numTribunal && t.getUf().toUpperCase().equals(uf)) {
				for (Vara v : t.getVaras())	{
					if (v.getNum() == numVara) {
						for (Processo p : v.getProcessos())	{
							if (p.getCodigo() == codigoProcesso) {
								for (Custo c : p.getCustos()){
									dados += c.toString() + "\n\n";
								}
								dados += "Total: " + df.format(p.calcularCusto());
							}
						}
					}
				}
			}
		}
		return dados;
	}

